// include libraries not part of the core language
// iostream is input-output stream from standard library
#include <iostream>


int main() {
    // << is the output operator
    // std:: indicates name is part of namespace std
    // std::cout is standard output stream
    std::cout << "hello" << std::endl;

    // expression contains operators and operands
    // expression followed by semicolon is expression statement
    // every operand has a type
    std::cout << "Hello, world!" << std::endl;
}

